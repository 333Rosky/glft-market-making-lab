EPSILON = 1e-12
QUEUE_MODEL_DESCRIPTION = (
    "conservative top-of-book/bookTicker queue-ahead approximation; not L2/FIFO reconstruction"
)
TIE_POLICY_DESCRIPTION = (
    "timestamp; market(sequence, book-before-trade exact tie); in-flight cancel; "
    "in-flight placement; external/closed-loop strategy request; "
    "zero-latency cancel then placement"
)


class Side(str, Enum):
    """Order or aggressor side."""

    BUY = "buy"
    SELL = "sell"

    @property
    def sign(self) -> int:
        return 1 if self is Side.BUY else -1

    @property
    def opposite(self) -> Side:
        return Side.SELL if self is Side.BUY else Side.BUY


class OrderStatus(str, Enum):
    PENDING = "pending"
    ACTIVE = "active"
    PARTIALLY_FILLED = "partially_filled"
    CANCEL_PENDING = "cancel_pending"
    FILLED = "filled"
    CANCELED = "canceled"
    REJECTED = "rejected"
    EXPIRED = "expired"


class Liquidity(str, Enum):
    MAKER = "maker"
    TAKER = "taker"


class AccountingModel(str, Enum):
    """Cash and mark-to-market convention used by the replay."""

    LINEAR = "linear"
    INVERSE = "inverse"


