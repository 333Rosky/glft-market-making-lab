"""Causal event-driven execution replay for GLFT experiments.

The replay deliberately separates theoretical GLFT quoting from empirical execution.
It consumes top-of-book and aggressive-trade events and never reconstructs information
that was not observable at the event timestamp.  Linear accounting settles cash in the
quote asset.  Inverse accounting treats quantities as contracts with a fixed quote-asset
face value, settles fees and PnL in the base asset, and rejects quantities that do not
respect the configured contract step.  Funding, margin requirements, and forced exchange
liquidations are outside this execution replay; ``liquidate_at_end`` is only a terminal
close at the observed touch.

Queue model
-----------
Only top-of-book (for example Binance ``bookTicker``) quantities are available.  The
``queue_ahead`` field is therefore an explicitly labelled approximation, not a claim of
full L2/FIFO reconstruction:

* joining the displayed best quote starts behind its displayed quantity;
* improving the best quote starts with zero external quantity ahead;
* an order away from the displayed best has unknown queue position and cannot consume an
  exact-price trade until that price is observed at the top of book (a print strictly
  through the limit is direct evidence that the resting order was crossed);
* displayed quantity reductions never improve an existing order's queue position because
  they may be cancellations behind it; only aggressive volume depletes queue ahead;
* cancellations of our own earlier orders do reduce the queue ahead of our later orders.

Tie policy
----------
Events are replayed in timestamp order, independently of input order.  At one timestamp:

1. market events run by source ``sequence`` (book before trade only on an exact tie);
2. cancel acknowledgements already in flight run;
3. placement acknowledgements already in flight run;
4. external and closed-loop strategy requests run by ``sequence``;
5. zero-latency acknowledgements generated by those requests run cancel-first.

Consequently a trade tied with activation is too early to fill the order, while a trade
tied with cancellation can still fill it.  This conservative convention prevents
same-timestamp lookahead when the source does not provide a total exchange sequence.
Sequence values from independent feeds are not a total order and must not be compared
directly; adapters must replace them with a synthetic order preserving the merged stream.
"""

